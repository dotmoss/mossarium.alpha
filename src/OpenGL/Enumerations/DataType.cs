namespace OpenGL;

public enum DataType : ushort
{
    Byte = GL_BYTE,
    UByte = GL_UNSIGNED_BYTE,
    Short = GL_SHORT,
    UShort = GL_UNSIGNED_SHORT,
    Int = GL_INT,
    UInt = GL_UNSIGNED_INT,
    Float = GL_FLOAT,
    I2 = GL_2_BYTES,
    I3 = GL_3_BYTES,
    I4 = GL_4_BYTES,
    Double = GL_DOUBLE
}